package output

import (
	"fmt"
	"io"
	"strings"

	"github.com/tochemey/kubewise/pkg/risk"
)

// RenderMarkdown writes the report as Markdown suitable for GitHub PR comments.
func RenderMarkdown(w io.Writer, report Report) error {
	var sb strings.Builder

	// Title
	sb.WriteString(fmt.Sprintf("## KubeWise: %s\n\n", report.ScenarioName))
	if report.ScenarioDesc != "" {
		sb.WriteString(fmt.Sprintf("_%s_\n\n", report.ScenarioDesc))
	}

	// Summary table
	sb.WriteString("| Metric | Value |\n")
	sb.WriteString("|--------|-------|\n")
	sb.WriteString(fmt.Sprintf("| Current monthly cost | %s |\n", formatCost(report.BaselineCost)))
	sb.WriteString(fmt.Sprintf("| Projected monthly cost | %s |\n", formatCost(report.ProjectedCost)))
	sb.WriteString(fmt.Sprintf("| **Savings** | **%s/mo (%.1f%%)** |\n", formatCost(report.Savings), report.SavingsPercent))
	sb.WriteString(fmt.Sprintf("| Cluster OOM risk | %.1f%% %s |\n", report.Risk.ClusterOOM*100, mdRiskEmoji(classifyOOMLevel(report.Risk.ClusterOOM))))
	sb.WriteString(fmt.Sprintf("| Overall risk | %s |\n", mdRiskBadge(report.Risk.OverallLevel)))
	sb.WriteString("\n")

	// Namespace breakdown in collapsible sections
	if len(report.NamespaceBreakdown) > 0 {
		sb.WriteString("<details>\n")
		sb.WriteString("<summary>Savings by namespace</summary>\n\n")
		sb.WriteString("| Namespace | Savings | Risk |\n")
		sb.WriteString("|-----------|---------|------|\n")
		for _, ns := range report.NamespaceBreakdown {
			sb.WriteString(fmt.Sprintf("| %s | %s/mo | %s |\n",
				ns.Namespace,
				formatCost(ns.Savings),
				mdRiskBadge(ns.RiskLevel),
			))
		}

		// Per-workload details if verbose
		if report.Verbose {
			sb.WriteString("\n### Workload details\n\n")
			sb.WriteString("| Namespace | Workload | Savings | Risk |\n")
			sb.WriteString("|-----------|----------|---------|------|\n")
			for _, ns := range report.NamespaceBreakdown {
				for _, wl := range ns.Workloads {
					sb.WriteString(fmt.Sprintf("| %s | %s | %s/mo | %s |\n",
						ns.Namespace,
						wl.Name,
						formatCost(wl.Savings),
						mdRiskBadge(wl.RiskLevel),
					))
				}
			}
		}

		sb.WriteString("\n</details>\n\n")
	}

	// Footer
	sb.WriteString("---\n")
	sb.WriteString("Generated by KubeWise\n")

	_, err := fmt.Fprint(w, sb.String())
	return err
}

// mdRiskEmoji returns a risk emoji for markdown.
func mdRiskEmoji(level risk.RiskLevel) string {
	switch level {
	case risk.RiskGreen:
		return "🟢"
	case risk.RiskAmber:
		return "🟡"
	case risk.RiskRed:
		return "🔴"
	default:
		return "⚪"
	}
}

// mdRiskBadge returns a risk badge string for markdown tables.
func mdRiskBadge(level risk.RiskLevel) string {
	switch level {
	case risk.RiskGreen:
		return "🟢 low"
	case risk.RiskAmber:
		return "🟡 moderate"
	case risk.RiskRed:
		return "🔴 high"
	case risk.RiskUnknown:
		return "⚪ unknown"
	default:
		return "⚪ unknown"
	}
}
